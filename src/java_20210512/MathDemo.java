package java_20210512;

public class MathDemo {
	public static void main(String[] args) {
		System.out.println(Math.ceil(3.1)); // double
		System.out.println(Math.floor(3.1)); // double
		System.out.println(Math.round(3.1)); // long
		System.out.println(Math.round(3.6)); // long
		System.out.println(Math.abs(-3.1)); // double
		System.out.println(Math.max(3, 2)); // integer
		System.out.println(Math.max(3.1, 2.3)); // double		
		System.out.println(Math.min(3, 2)); // integer
		System.out.println(Math.min(3.1, 2.3)); // double
		System.out.println(Math.sqrt(3)); // double
		System.out.println(Math.pow(2, 3)); // double
		System.out.println((int)(Math.random() * 45)); // double
		
	}
	
}
